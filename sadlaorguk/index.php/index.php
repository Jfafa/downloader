                       <h2>Welcome</h2>
                       <p>Sheffield and District Landlord Association (SADLA) is a non-profit making organisation of private landlords providing a united front to represent their interests.</p>
<p>SADLA are associate members of the <a title="Residential Landlords Association" href="http://www.rla.org.uk/" target="_blank">Residential Landlord Association</a>. This is a nationally recognised body of residential landlords which is consulted by the Government, Judiciary and Media.</p>
<p>Our <a href="/index.php/membership-benefits">membership</a> covers all types of landlord no matter how large or small their portfolio. <a title="Link to the Join Form" href="/index.php/join">Join</a> SADLA and help develop the Association in a way that can benefit your business.</p>
